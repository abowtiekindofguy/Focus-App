class ProfilePage extends StatelessWidget {
    final String currentUserId;
   ProfilePage({required this.currentUserId});

  @override
  Widget build(BuildContext context) {
      final friendsRef = FirebaseFirestore.instance.collection('users').doc(currentUserId);

    return Scaffold(
      body: Column(
        children: [
          const Flexible(flex: 2, child: _TopPortion()),
          Flexible(
            flex: 3,
            child: Padding(
              padding: const EdgeInsets.all(8.0),
              child: Column(
                children: [
                  Text(
                    "Richie Lorie",
                    style: Theme.of(context)
                        .textTheme
                        .headline6
                        ?.copyWith(fontWeight: FontWeight.bold),
                  ),
                  const SizedBox(height: 16),
                  Row(
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: [
                      FloatingActionButton.extended(
                        onPressed: () {},
                        heroTag: 'follow',
                        elevation: 0,
                        label: const Text("Friend"),
                        icon: const Icon(Icons.person_2),
                      ),
                      const SizedBox(width: 16.0),
                      FloatingActionButton.extended(
                        onPressed: () {},
                        heroTag: 'mesage',
                        elevation: 0,
                        backgroundColor: Colors.red,
                        label: const Text("Challenge"),
                        icon: const Icon(Icons.cabin),
                      ),
                    ],
                  ),
                  const SizedBox(height: 16),
                  const _ProfileInfoRow(),
                  StreamBuilder<DocumentSnapshot>(
            stream: friendsRef.snapshots(),
            builder: (context, snapshot) {
              if (!snapshot.hasData) {
                return Center(child: CircularProgressIndicator());
              }
              var data = snapshot.data?.data() as Map<String, dynamic>?;
              var friendsMap = data?['friends'] as Map<String, dynamic>?;
              if (friendsMap == null || friendsMap.isEmpty) {
                return Center(child: Text('No friends found.'));
              }

              List<String> friends = friendsMap.keys.toList();

              return ListView.builder(
                itemCount: friends.length,
                itemBuilder: (context, index) {
                  String friendUserId = friends[index];
                  return ListTile(
                    title: Text("Friend ID: $friendUserId"),
                  );
                },
              );
            },
          ),

                ],
              ),
            ),
          ),


        //   Flexible( // Wrap the StreamBuilder in an Expanded widget
        //   child: 
        // ),  
        ],
      ),
    );
  }
}

class _ProfileInfoRow extends StatelessWidget {
  const _ProfileInfoRow({Key? key}) : super(key: key);

  final List<ProfileInfoItem> _items = const [
    ProfileInfoItem("Posts", 900),
    ProfileInfoItem("Followers", 120),
    ProfileInfoItem("Following", 200),
  ];

  @override
  Widget build(BuildContext context) {
    return Container(
      height: 80,
      constraints: const BoxConstraints(maxWidth: 400),
      child: Row(
        mainAxisAlignment: MainAxisAlignment.spaceEvenly,
        children: _items
            .map((item) => Expanded(
                    child: Row(
                  children: [
                    if (_items.indexOf(item) != 0) const VerticalDivider(),
                    Expanded(child: _singleItem(context, item)),
                  ],
                )))
            .toList(),
      ),
    );
  }

  Widget _singleItem(BuildContext context, ProfileInfoItem item) => Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          Padding(
            padding: const EdgeInsets.all(8.0),
            child: Text(
              item.value.toString(),
              style: const TextStyle(
                fontWeight: FontWeight.bold,
                fontSize: 20,
              ),
            ),
          ),
          Text(
            item.title,
            style: Theme.of(context).textTheme.caption,
          )
        ],
      );
}

class ProfileInfoItem {
  final String title;
  final int value;
  const ProfileInfoItem(this.title, this.value);
}

